// 2. Helper – convert main_courses text to lowercase string
const coursesToText = (main_courses) => {
  if (!main_courses) return "";
  if (Array.isArray(main_courses)) {
    return main_courses.join(" ").toLowerCase();
  }
  return String(main_courses).toLowerCase();
};

// 3. For one college, find all matching stream keys
const getStreamsForCollege = (college) => {
  const text = coursesToText(college.main_courses);
  const matched = [];

  Object.entries(STREAM_KEYWORDS).forEach(([streamKey, keywords]) => {
    const isMatch = keywords.some((kw) => text.includes(kw));
    if (isMatch) matched.push(streamKey);
  });

  return matched;
};

